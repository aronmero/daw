    protected void recargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ISBN");
        modelo.addColumn("Titulo");
        modelo.addColumn("Autor");
        modelo.addColumn("Año Publicacion");
        modelo.addColumn("Editorial");
        modelo.addColumn("Numero");
        String[] parts = new String[6];
        for (Material material : materiales) {
            parts[0] = material.getIsbn();
            parts[1] = material.getTitulo();
            parts[2] = material.getAutor();
            parts[3] = String.valueOf(material.getAnoDePublicacion());
            parts[4] = null;
            parts[5] = null;
            if (material instanceof Libro) {
                parts[4] = ((Libro) material).getEditorial();
            }
            if (material instanceof Revista) {
                parts[5] = String.valueOf(((Revista) material).getNumeroDeEdicion());
            }
            modelo.addRow(parts);
        }
        tablaMateriales.setModel(modelo);
    }

