    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int numeroRow = tablaMateriales.getSelectedRow();
        if (numeroRow > -1) {
            String isbn = (String) tablaMateriales.getValueAt(numeroRow, 0);
            for (Material material : materiales) {
                if (isbn.equalsIgnoreCase(material.getIsbn())) {
                    materiales.remove(material);
                    recargarTabla();
                    break;
                }
            }
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int numeroRow = tablaMateriales.getSelectedRow();
        if (numeroRow > -1) {
            String isbn = (String) tablaMateriales.getValueAt(numeroRow, 0);
            for (Material material : materiales) {
                if (isbn.equalsIgnoreCase(material.getIsbn())) {
                    int indice = materiales.indexOf(material);
                    if (material instanceof Libro) {
                        setContentPane(new PanelEditar(this, 0, material, indice));
                        pack();
                        break;
                    } else if (material instanceof Revista) {
                        setContentPane(new PanelEditar(this, 1, material, indice));
                        pack();
                        break;
                    }

                }
            }

        }
    }//GEN-LAST:event_btnEditarActionPerformed

